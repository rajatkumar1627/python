def send_postcard(address, message):
    print("Sending a postcard to", address)
    print("With the message:", message)


send_postcard("Kankarbagh, Patna", "Hello, bro!")
# Sending a postcard to Kankarbagh, Patna
# With the message: Hello, bro!

send_postcard("Boring Road, Patna", "Hi, Boring Road!")
# Sending a postcard to Boring Road, Patna
# With the message: Boring Road!